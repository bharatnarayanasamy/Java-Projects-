package jrJava.basics;

public class WhatIsVariable {

	public static void main(String[] args) {

		int x;
		x = 5;
		System.out.println(x);
		
		
		//5 = x;
		//x + 1 = 5;
		
		x = x + 1;
		System.out.println(x);
		x = 2*x + 3;
		System.out.println(x);
		x = 3*x - 7;		
		System.out.println(x);
		
		
		
		
	}

}
